def _gen_tapping(prog, tool, tool_diam, wp, params, holes, features):
    """攻丝 - G84 右旋攻丝"""
    tool_diameter = tool_diam
    prog.tool_calls.append(f"T{tool.tool_number} M6 ({tool.type} D={tool.diameter})")
    prog.tool_calls.append(f"M3 S{int(params['spindle_speed'])}")
    prog.tool_calls.append(f"G94 G95")  # 每转进给
    
    moves = prog.moves
    z_safe = wp.z_origin + 5.0
    z_r = wp.z_origin + 2.0
    pitch = tool_diameter * 0.075 if tool_diameter < 12 else tool_diameter * 0.0625
    
    if holes:
        for i, h in enumerate(holes, 1):
            hx, hy, hdepth = h.get("x", 0), h.get("y", 0), h.get("depth", 10)
            moves.append(f"; === 攻丝{i}: X={hx:.3f} Y={hy:.3f} 螺距={pitch:.3f} ===")
            moves.append(f"G0 Z{z_safe:.3f}")
            moves.append(f"G0 X{hx:.3f} Y{hy:.3f}")
            moves.append(f"G84 Z{wp.z_origin - hdepth:.3f} R{z_r:.3f} F{pitch:.3f}")
            moves.append("G80")
    
    moves.append("M5")

