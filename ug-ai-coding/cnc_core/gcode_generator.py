def _gen_turning(prog, tool, wp, params, holes, features):
    """车削 - 外圆车削"""
    prog.tool_calls.append(f"T{tool.tool_number} M6 ({tool.type} D={tool.diameter})")
    prog.tool_calls.append(f"M3 S{int(params['spindle_speed'])}")
    prog.tool_calls.append(f"G97")
    
    moves = prog.moves
    feed = params['feed_rate']
    radial = params['radial_depth']
    
    # 端面加工
    moves.append(f"G0 X0 Z5.0")
    moves.append(f"G1 X{wp.height:.3f} F{feed:.0f}")
    moves.append(f"G0 X0 Z5.0")
    
    # 外圆车削
    current_d = wp.height
    end_d = 10.0
    while current_d > end_d:
        current_d = max(current_d - radial, end_d)
        moves.append(f"G0 X{current_d:.3f} Z5.0")
        moves.append(f"G1 Z0 F{feed:.0f}")
        moves.append(f"G1 Z{wp.length:.3f} F{feed:.0f}")
        moves.append(f"G1 X{current_d - radial:.3f}")
    
    moves.append("M5")

def _gen_thread_turning(prog, tool, tool_diam, wp, params, holes, features):
    """螺纹车削"""
    prog.tool_calls.append(f"T{tool.tool_number} M6 ({tool.type} D={tool.diameter})")
    prog.tool_calls.append(f"M3 S{int(params['spindle_speed'])}")
    prog.tool_calls.append(f"G96 S120")  # 恒线速
    
    moves = prog.moves
    pitch = tool_diam * 0.075
    num_threads = int(wp.height / (3.5 * pitch))
    
    moves.append(f"G0 X{wp.height + 5:.3f} Z5.0")
    moves.append(f"G32 Z-{wp.length:.3f} F{pitch:.3f}")
    moves.append(f"G0 X{wp.height + 5:.3f} Z5.0")
    
    for i in range(num_threads):
        depth = 0.5 * pitch * (1 - (i + 1) / (num_threads + 1)) ** 2
        moves.append(f"G0 X{wp.height - 2 * depth:.3f}")
        moves.append(f"G32 Z-{wp.length:.3f} F{pitch:.3f}")
        moves.append(f"G0 X{wp.height + 5:.3f} Z5.0")
    
    moves.append("M5")

def _gen_grooving(prog, tool, wp, params, holes, features):
    """切槽"""
    prog.tool_calls.append(f"T{tool.tool_number} M6 ({tool.type} D={tool.diameter})")
    prog.tool_calls.append(f"M3 S{int(params['spindle_speed'])}")
    
    moves = prog.moves
    feed = params['feed_rate']
    
    # 根据特征或holes定位槽
    if features:
        for i, f in enumerate(features, 1):
            gx, gw = f.get("x", 0), f.get("width", 2)
            moves.append(f"; === 槽{i}: X={gx:.3f} W={gw:.3f} ===")
            moves.append(f"G0 X{gx + gw / 2:.3f} Z{wp.length + 5:.3f}")
            moves.append(f"G1 Z{wp.z_origin - 10:.3f} F{feed * 0.3:.0f}")
            moves.append(f"G1 X{gx - gw / 2:.3f} F{feed * 0.5:.0f}")
            moves.append(f"G1 X{gx + gw / 2:.3f}")
            moves.append(f"G0 Z{wp.length + 5:.3f}")
    else:
        moves.append(f"G0 X{tool.diameter} Z5.0")
        moves.append(f"G1 Z-10 F{feed * 0.3:.0f}")
        moves.append(f"G0 Z{wp.length + 5:.3f}")
    
    moves.append("M5")
